let value

// operadores de atribuição
value = 1

console.log(value) // 1

// incremento
value += 1 // value = value + 1
console.log(value) // 2

// decremento
value -= 1 // value = value - 1
console.log(value) // 1


// multiplicação
value *= 2 // value = value * 2


// Resto da divisão
value %= 2 // value = value % 2
console.log(value) // 1


// exponenciação
value **= 2 // value = value ** 2