let one = 1;
let two = 2;

// === Estritamente Igual e Diferente
console.log(one === 1); // true
console.log(one === "1"); // false
console.log(two === 2); // true
console.log(two === "2"); // false

// !== Estritamente Diferente de
console.log(one !== 1); // false
console.log(one !== "1"); // true
console.log(two !== 2); // false
console.log(two !== "2"); // true